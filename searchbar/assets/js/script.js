//First Picture
function hover1() {
    document.querySelector('.di2').style.opacity = "50%"
    document.querySelector('.di3').style.opacity = "50%"
    document.querySelector('.di4').style.opacity = "50%"
}
function hover1leave() {
    document.querySelector('.di2').style.opacity = "100%"
    document.querySelector('.di3').style.opacity = "100%"
    document.querySelector('.di4').style.opacity = "100%"
}

